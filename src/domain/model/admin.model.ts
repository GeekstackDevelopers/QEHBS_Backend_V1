import { Schema, model, Document } from "mongoose";
import { IAdmin } from "../entity/admin";

const adminSchema = new Schema<IAdmin>(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    name: {
      type: String,
      required: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const AdminModel = model<IAdmin>("Admin", adminSchema);

export default AdminModel;
